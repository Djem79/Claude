function articleToHtml(content: string): string {
  return content
    .split(/\n\n+/)
    .map(p => {
      const t = p.trim()
      if (!t) return ''
      if (t.startsWith('## ')) return `<h2>${escapeHtml(t.slice(3))}</h2>`
      return `<p>${escapeHtml(t).replace(/\n/g, '<br/>')}</p>`
    })
    .filter(Boolean)
    .join('\n')
}

